    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txtContrasena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe escribir la contrase単a del usuario "+us.getUsuario()+" para desbloquear el terminal");
            txtContrasena.requestFocus();
        }else{
            if(txtContrasena.getText().compareTo(us.getContrasena()) == 0) {
                if(pm != null) {
                    pm.setVisible(true);
                    this.dispose();
                    pm = null;
                }else if(pa != null) {
                    pa.setVisible(true);
                    this.dispose();
                    pa = null;
                }else if(pr != null) {
                    pr.setVisible(true);
                    this.dispose();
                    pr = null;
                }
            }else{
                JOptionPane.showMessageDialog(null, "Contrase単a Incorrecta");
                txtContrasena.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

